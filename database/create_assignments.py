# ---------- Permanent Makeup Job ----------

def make_makeup_job_assignments_to_db():
    """
    Create permanent Makeup Job (job_id = 0) and assign once per user.
    """
    # --- Ensure Makeup Job exists ---
    existing = execute_query(
        "jobs",
        "select",
        filters=[("job_id", "eq", 0)],
    )

    if not existing:
        execute_query(
            "jobs",
            "insert",
            data={
                "job_id": 0,
                "job_name": "Makeup Job",
                "job_description": "Permanent makeup job for all users",
                "num_hours": 1,
                "job_type": "MAKEUP",
                "due_by_time": "23:59:59",
            },
        )
        print("Created permanent Makeup Job.")

    users = get_table("users").select("*").execute().data
    far_future_due = datetime(
        2026, 12, 31, 23, 59, 0, tzinfo=ET_OFFSET
    ).isoformat()

    for user in users:
        user_id = user["user_id"]

        # Check if user already has a makeup assignment
        active = execute_query(
            "active_assignments",
            "select",
            filters=[("user_id", "eq", user_id)],
        )

        has_makeup = False
        for a in active:
            mapping = execute_query(
                "assignment_jobs",
                "select",
                filters=[("assignment_id", "eq", a["assignment_id"])],
            )
            if mapping and mapping[0]["job_id"] == 0:
                has_makeup = True
                break

        if has_makeup:
            continue

        # Create mapping (assignment_id generated by Postgres)
        assignment_row = execute_query(
            "assignment_jobs",
            "insert",
            data={"job_id": 0},
        )[0]

        # Create active assignment
        execute_query(
            "active_assignments",
            "insert",
            data={
                "assignment_id": assignment_row["assignment_id"],
                "user_id": user_id,
                "due_at": far_future_due,
                "status": "ASSIGNED",
            },
        )

    print("Permanent Makeup Job assigned where missing.")
